let testNumber = prompt('Please write a number in range 0 to 9');

if (testNumber == 1) {
    document.write('!');
} else if (testNumber == 2) {
    document.write('@');     
} else if (testNumber == 3) {
    document.write('#'); 
} else if (testNumber == 4) {
    document.write('$');
} else if (testNumber == 5) {
    document.write('%');
} else if (testNumber == 6) {
    document.write('^');
} else if (testNumber == 7) {
    document.write('&');
} else if (testNumber == 8) {
    document.write('*');
} else if (testNumber == 9) {
    document.write('(');
} else if (testNumber == 0) {
    document.write(')');
}